    public static string? GetKeyName(this int keyCode)
    {
        return typeof(Keycodes)
            .GetFields()
            .Where(f => f.IsLiteral && f.FieldType == typeof(int))
            .FirstOrDefault(fi => fi.GetRawConstantValue()!.Equals(keyCode))?
            .Name;
    }

    public static string? GetKeyName(this ushort keyCode)
    {
        return GetKeyName((int)keyCode);
    }

    public static int? GetKeyValue(this string name)
    {
        var name1 = name.ToLower().Trim();
        var value = typeof(Keycodes)
            .GetFields()
            .Where(f => f.IsLiteral && f.FieldType == typeof(int))
            .FirstOrDefault(fi => fi.Name.ToLower() == name1);

        if (value == null)
        {
            return null;
        }
        return (int)value.GetRawConstantValue()!;
    }

